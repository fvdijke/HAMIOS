from __future__ import annotations
from PySide6.QtCore import QObject, Signal

# ── Language signal emitter ───────────────────────────────────────────────────

class _LangEmitter(QObject):
    changed = Signal(str)   # emits new language code

_emitter = _LangEmitter()
language_changed = _emitter.changed   # import this to connect slots

# ── State ─────────────────────────────────────────────────────────────────────

_LANG: str = "en"

def get_language() -> str:
    return _LANG

def set_language(lang: str):
    global _LANG
    _LANG = lang if lang in ("en", "nl") else "nl"
    _emitter.changed.emit(_LANG)

def tr(key: str, **kwargs) -> str:
    """Return translated string for the current language."""
    d = _S.get(_LANG, _S["nl"])
    s = d.get(key) or _S["nl"].get(key) or key
    return s.format(**kwargs) if kwargs else s

